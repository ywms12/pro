document.getElementById("showEle").addEventListener("click",function(){
    document.getElementById("showEle").style.display = "none";
    document.getElementById("element").style.display = "block";
});
document.getElementById("hideEle").addEventListener("click",function(){
    document.getElementById("showEle").style.display = "block";
    document.getElementById("element").style.display = "none";
});
